// WHILE LOOP

let number = 1;

while (number <= 50) {
  // code
  let message;

  if (number % 2 === 0) {
    message = "Even";
  } else {
    message = "Odd";
  }

  console.log(`Number : ${number} is ${message}`);

  // incrementer
  number = number + 1;
}

